    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(p!=null && checkData()){
            DatabaseConnection.getInstance().addVisit(jTextField1.getText(), jTextArea1.getText(),p.getPatientId());
            DatabaseConnection.getInstance().getVisits(jTable1,p.getPatientId());
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        if(currentVisitId>0){
            DatabaseConnection.getInstance().removeVisit(currentVisitId);
            DatabaseConnection.getInstance().getVisits(jTable1,p.getPatientId());
        }
    }//GEN-LAST:event_jButton4MouseClicked

    private void jButton3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MousePressed
         if(currentVisitId>0 && checkData()){
            DatabaseConnection.getInstance().updateVisit(createVisit());
            DatabaseConnection.getInstance().getVisits(jTable1,p.getPatientId());
        }
    }//GEN-LAST:event_jButton3MousePressed

    private Visit createVisit(){
        return new Visit(currentVisitId,jTextArea1.getText(),Date.valueOf(jTextField1.getText()));   
    }
    
    private boolean checkData(){
        if(jTextArea1.getText().length()<1){
            jLabel4.setText("wprowadz dane wizty");
            return false;
        }
        try{
            Date.valueOf(jTextField1.getText());
        }
        catch(Exception e){
            jLabel4.setText("Format daty (yyyy-mm-dd)!");
            return false;
        }
        jLabel4.setText("");
        return true;
    }

