    public PatientInfoPanel(NewJFrame jf, String pesel) {
        initComponents();
        this.jf=jf;
        p =DatabaseConnection.getInstance().getPatient(pesel);
        if(p!=null){
            jLabel1.setText(p.getFirstName() + " " + p.getLastName());
            jLabel2.setText(p.getAddress());
            jLabel3.setText(p.getPesel());
            DatabaseConnection.getInstance().getVisits(jTable1,p.getPatientId());
        }
        
        jTable1.getSelectionModel().addListSelectionListener((ListSelectionEvent event) -> {
            try {
                if (jTable1.getSelectedRow() >= 0) {
                    currentVisitId=(int)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    Object date = jTable1.getValueAt(jTable1.getSelectedRow(), 1);
                    Object description = jTable1.getValueAt(jTable1.getSelectedRow(), 2);

                    jTextField1.setText(date.toString());
                    jTextArea1.setText(description.toString());
                }
                else{
                    currentVisitId=-1;
                }
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        });
        
    }

